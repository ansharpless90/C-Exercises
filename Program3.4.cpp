//Program starts at 0 and increments by 1
//each time it is displayed.
#include <iostream>
using namespace std;

int main()
{

int count;

count = 0;
cout <<"The initial value of count is " << count <<endl;
count++;
cout<<"  count is now " <<count <<endl;
count ++;
cout<<"  count is now " <<count <<endl;
count++;
cout<<"  count is now " <<count <<endl;
count++;
cout<<"  count is now " <<count <<endl;




return 0;
}
